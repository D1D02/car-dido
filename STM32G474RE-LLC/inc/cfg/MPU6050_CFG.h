#ifndef INC_CFG_MPU6050_CFG_H_
#define INC_CFG_MPU6050_CFG_H_

#define MPU6050_ADDR            0x68 // 0x69 if the AD0 is HIGH
#define MPU6050_DATA_START      0x3B // Indirizzo di ACCEL_XOUT_H

#define SMPLRT_DIV_CFG_REG      0x19
#define SMPLRT_DIV_CFG_REG_VAL  0x09 // Sample Rate = 1000 / (1 + 9) = 100Hz

#define MPU6050_LPF_CFG_REG     0x1A

#define MPU6050_LPF_0_DEL_0     0x0
#define MPU6050_LPF_1_DEL_2     0x1
#define MPU6050_LPF_2_DEL_3     0x2
#define MPU6050_LPF_3_DEL_5     0x3
#define MPU6050_LPF_4_DEL_8_5   0x4
#define MPU6050_LPF_5_DEL_13_5  0x5
#define MPU6050_LPF_6_DEL_19    0x6

#define MPU6050_LPF_CFG_VAL     MPU6050_LPF_3_DEL_5

#define MPU6050_GYRO_RAD_CFG_REG 0x1B

#define MPU6050_GYROSCOPE_250d_s  0x00
#define MPU6050_GYROSCOPE_500d_s  0x08
#define MPU6050_GYROSCOPE_1000d_s 0x10
#define MPU6050_GYROSCOPE_2000d_s 0x18

#define MPU6050_GYROSCOPE_CFG_VAL MPU6050_GYROSCOPE_250d_s

#if MPU6050_GYROSCOPE_CFG_VAL == MPU6050_GYROSCOPE_250d_s
  #define MPU6050_GYRO_LSB_SENS   131.0f
#elif MPU6050_GYROSCOPE_CFG_VAL == MPU6050_GYROSCOPE_500d_s
  #define MPU6050_GYRO_LSB_SENS   65.5f
#elif MPU6050_GYROSCOPE_CFG_VAL == MPU6050_GYROSCOPE_1000d_s
  #define MPU6050_GYRO_LSB_SENS   32.8f
#elif MPU6050_GYROSCOPE_CFG_VAL == MPU6050_GYROSCOPE_2000d_s
  #define MPU6050_GYRO_LSB_SENS   16.4f
#endif

#define MPU6050_ACC_G_CFG_REG   0x1C

#define MPU6050_ACCELEROMETER_2g  0x00
#define MPU6050_ACCELEROMETER_4g  0x08
#define MPU6050_ACCELEROMETER_8g  0x10
#define MPU6050_ACCELEROMETER_16g 0x18

#define MPU6050_ACCELEROMETER_CFG_VAL MPU6050_ACCELEROMETER_2g

#if MPU6050_ACCELEROMETER_CFG_VAL == MPU6050_ACCELEROMETER_2g
  #define MPU6050_ACCELEROMETER_LSB_SENS 16384.0f
#elif MPU6050_ACCELEROMETER_CFG_VAL == MPU6050_ACCELEROMETER_4g
  #define MPU6050_ACCELEROMETER_LSB_SENS 8192.0f
#elif MPU6050_ACCELEROMETER_CFG_VAL == MPU6050_ACCELEROMETER_8g
  #define MPU6050_ACCELEROMETER_LSB_SENS 4048.0f
#elif MPU6050_ACCELEROMETER_CFG_VAL == MPU6050_ACCELEROMETER_16g
  #define MPU6050_ACCELEROMETER_LSB_SENS 2048.0f
#endif

#endif /* INC_CFG_MPU6050_CFG_H_ */
