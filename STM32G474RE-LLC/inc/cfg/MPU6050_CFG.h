#ifndef INC_CFG_MPU6050_CFG_H_
#define INC_CFG_MPU6050_CFG_H_

#define MPU6050_ADDR 0x68 //0x69 if the AD0 is HIGH

#define SMPLRT_DIV_CFG_REG 0x19
#define SMPLRT_DIV_CFG_REG_VAL 0x09 //Sample Rate = Gyroscope Output Rate / (1 + SMPLRT_DIV)

#define MPU6050_LPF_CFG_REG 0x1A
typedef enum {
  MPU6050_LPF_0_DEL_0 = 0x0,
  MPU6050_LPF_1_DEL_2 = 0x1,
  MPU6050_LPF_2_DEL_3 = 0x2,
  MPU6050_LPF_3_DEL_5 = 0x3,
  MPU6050_LPF_4_DEL_8_5 = 0x4,
  MPU6050_LPF_5_DEL_13_5 = 0x5,
  MPU6050_LPF_6_DEL_19 = 0x6,
}MPU6050_LPF_CFG;

#define MPU6050_GYRO_RAD_CFG_REG 0x1B
typedef enum {
  MPU6050_GYROSCOPE_250d_s = 0x00,
  MPU6050_GYROSCOPE_500d_s = 0x08,
  MPU6050_GYROSCOPE_1000d_s = 0x10,
  MPU6050_GYROSCOPE_2000d_s = 0x18,
}MPU6050_GYROSCOPE_CFG;

#define MPU6050_ACC_G_CFG_REG 0x1C
typedef enum {
  MPU6050_ACCELEROMETER_2g = 0x00,
  MPU6050_ACCELEROMETER_4g = 0x08,
  MPU6050_ACCELEROMETER_8g = 0x10,
  MPU6050_ACCELEROMETER_16g = 0x18,
}MPU6050_ACCELEROMETER_CFG;




#endif /* INC_CFG_MPU6050_CFG_H_ */
